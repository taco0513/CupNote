export type IconName = keyof typeof ICON_REGISTRY

interface IconProps {
  name: IconName
  className?: string
  size?: number
  color?: string
  strokeWidth?: number
}

/**
 * Optimized Icon Component
 * Uses centralized registry for better tree-shaking
 */
export function Icon({ 
  name, 
  className = 'w-4 h-4', 
  size, 
  color, 
  strokeWidth = 2 
}: IconProps) {
  const IconComponent = ICON_REGISTRY[name] as LucideIcon
  
  if (!IconComponent) {
    console.warn(`Icon "${name}" not found in registry`)
    return null
  }
  
  return (
    <IconComponent
      className={className}
      size={size}
      color={color}
      strokeWidth={strokeWidth}
    />
  )
}

